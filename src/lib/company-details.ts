export function getCompanyServices(company: Company) {
  if (company.name.includes('TOEFL') || company.name.includes('GRE')) {
    return ['TOEFL/GRE шалгалтын бэлтгэл', 'Оношлох тест', 'Онооны зорилго тодорхойлох', 'Сургалтын төлөвлөгөө гаргах']
  }

  if (company.name.includes('E-Pen')) {
    return ['Гадаадад суралцах зөвлөгөө', 'Сургууль сонголт', 'Эсээ болон CV зөвлөмж', 'Тэтгэлэг, элсэлтийн материалын бэлтгэл']
  }

  return categoryServices[company.category] ?? categoryServices['Бусад']
}

export function getCompanyAudience(company: Company) {
  return categoryAudience[company.category] ?? categoryAudience['Бусад']
}

